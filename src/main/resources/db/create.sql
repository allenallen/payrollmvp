CREATE TABLE PUBLIC."USER"
(
    USERNAME VARCHAR_IGNORECASE,
    PASSWORD VARCHAR_IGNORECASE,
    ID       BIGINT AUTO_INCREMENT,
    CONSTRAINT USER_PK PRIMARY KEY (ID)
);
